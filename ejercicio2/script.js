function resta(n1, n2){
    return n1 - n2;
}

function multiplicacion(n1, n2){
    return n1 * n2;
}

function division(n1, n2){
    return n1 / n2
}

let resultado1 = resta(4, 10);
let resultado2 = multiplicacion(6, 10);
let resultado3 = division(10, 5);

alert('Resta: '+ resultado1 + '\nMultiplicacion: ' + resultado2 + '\nDivision: '+ resultado3)